package com.rj.mobile.android.pogo;

public class DeviceModel {
	
	private String deviceId;
	private String deviceName;
	private String deviceOs;
	private String deviceOsVersion;
	private String deviceIp;
	private int deviceTcpPort;
	private int deviceForwardPort;
	private int deviceStatus;
	
	public String getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}
	public String getDeviceName() {
		return deviceName;
	}
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}
	
	public String getDeviceOs() {
		return deviceOs;
	}
	public void setDeviceOs(String deviceOs) {
		this.deviceOs = deviceOs;
	}
	public String getDeviceOsVersion() {
		return deviceOsVersion;
	}
	public void setDeviceOsVersion(String deviceOsVersion) {
		this.deviceOsVersion = deviceOsVersion;
	}
	public String getDeviceIp() {
		return deviceIp;
	}
	public void setDeviceIp(String deviceIp) {
		this.deviceIp = deviceIp;
	}
	public int getDeviceTcpPort() {
		return deviceTcpPort;
	}
	public void setDeviceTcpPort(int deviceTcpPort) {
		this.deviceTcpPort = deviceTcpPort;
	}
	public int getDeviceForwardPort() {
		return deviceForwardPort;
	}
	public void setDeviceForwardPort(int deviceForwardPort) {
		this.deviceForwardPort = deviceForwardPort;
	}
	public int getDeviceStatus() {
		return deviceStatus;
	}
	public void setDeviceStatus(int deviceStatus) {
		this.deviceStatus = deviceStatus;
	}
	

	 
 
 
}
